package co.grandcircus.FinalProject.entity;


public class Agent {
	 
	    public String id;
	    public Photo photo;
	    public String name;
		public String getId() {
			return id;
		}
		public void setId(String id) {
			this.id = id;
		}
		public Photo getPhoto() {
			return photo;
		}
		public void setPhoto(Photo photo) {
			this.photo = photo;
		}
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
	    
	    
}
